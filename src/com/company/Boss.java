package com.company;

public class Boss {
    private int health;
    private int damage;
    private String chooseBossDefends;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getChooseBossDefends() {
        return chooseBossDefends;
    }

    public void setChooseBossDefends(String chooseBossDefends) {
        this.chooseBossDefends = chooseBossDefends;
    }
}
